namespace XmobiTea.ProtonNet.Server.WebApi.Types
{
    /// <summary>
    /// Provides constant header names used in the ProtonNet server.
    /// </summary>
    static class HeaderNames
    {
        /// <summary>
        /// Represents the header name for session ID.
        /// </summary>
        public const string SessionId = "xp-sid";

        /// <summary>
        /// Represents the header name for encryption key.
        /// </summary>
        public const string EncryptKey = "xp-ek";

        /// <summary>
        /// Represents the header name for token.
        /// </summary>
        public const string Token = "xp-token";

    }

}
