namespace XmobiTea.Data.Converter.Types
{
    /// <summary>
    /// Enumeration representing various field data types.
    /// </summary>
    public enum FieldDataType
    {
        /// <summary>
        /// Represents a boolean data field.
        /// </summary>
        Boolean = 0,

        /// <summary>
        /// Represents a numeric data field.
        /// </summary>
        Number = 1,

        /// <summary>
        /// Represents a string data field.
        /// </summary>
        String = 2,

        /// <summary>
        /// Represents an array data field.
        /// </summary>
        Array = 3,

        /// <summary>
        /// Represents an object data field.
        /// </summary>
        Object = 4,

    }

}
