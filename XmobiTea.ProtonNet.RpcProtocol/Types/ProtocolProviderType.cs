    /// </summary>
    public enum ProtocolProviderType : byte
    {
        /// <summary>
        /// SimplePack protocol provider.
        /// </summary>
        SimplePack = 0,

        /// <summary>
        /// MessagePack protocol provider.
        /// </summary>
        MessagePack = 1,

    }

}
