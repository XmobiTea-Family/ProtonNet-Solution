namespace XmobiTea.ProtonNetCommon.Types
{
    /// <summary>
    /// This static class provides commonly used character constants.
    /// </summary>
    public static class SpecialChars
    {
        /// <summary>
        /// Represents the newline character ('\n').
        /// </summary>
        public static readonly char NewLine = '\n';

        /// <summary>
        /// Represents the carriage return character ('\r').
        /// </summary>
        public static readonly char CarriageReturn = '\r';

        /// <summary>
        /// Represents the horizontal tab character ('\t').
        /// </summary>
        public static readonly char HorizontalTab = '\t';

        /// <summary>
        /// Represents the vertical tab character ('\v').
        /// </summary>
        public static readonly char VerticalTab = '\v';

        /// <summary>
        /// Represents the space character (' ').
        /// </summary>
        public static readonly char Spacebar = ' ';

        /// <summary>
        /// Represents the colon character (':').
        /// </summary>
        public static readonly char Colon = ':';

        /// <summary>
        /// Represents the semicolon character (';').
        /// </summary>
        public static readonly char Semicolon = ';';

        /// <summary>
        /// Represents the digit '0' character.
        /// </summary>
        public static readonly char Number0 = '0';

        /// <summary>
        /// Represents the digit '9' character.
        /// </summary>
        public static readonly char Number9 = '9';

        /// <summary>
        /// Represents the question mark character ('?').
        /// </summary>
        public static readonly char QuestionMark = '?';

        /// <summary>
        /// Represents the forward slash character ('/').
        /// </summary>
        public static readonly char Slash = '/';

        /// <summary>
        /// Represents the backslash character ('\\').
        /// </summary>
        public static readonly char Backslash = '\\';

        /// <summary>
        /// Represents the equals sign character ('=').
        /// </summary>
        public new static readonly char Equals = '=';

    }

}
